
let backgroundcolor = document.getElementById("totalbackgroundcolor");
let text = document.getElementById("text-editor");



function tochangebackgroundcolorgray(){
    backgroundcolor.style.backgroundColor="#e0e0e0";
    text.textContent = "#e0e0e0";
}


function tochangebackgroundcolorgreen(){
    backgroundcolor.style.backgroundColor="#6fcf97";
    text.textContent = "#6fcf97";
}

function tochangebackgroundcolorblue(){
    backgroundcolor.style.backgroundColor="#56ccf2";
    text.textContent = "#56ccf2";
}

function tochangebackgroundcolorpink(){
    backgroundcolor.style.backgroundColor="#bb6bd9";
    text.textContent = "#bb6bd9";
}